// setTimeout() ফাংশন এর কাজ হচ্ছে, আমি যদি একটি কাজকে একটি নির্দিষ্ট সময় পর করাতে চাই তাহলে সেক্ষেত্রে এটি ব্যবহার করব।
// অর্থাৎ আমার কতগুলো কাজ করার পর তারপর setTimeout() দিয়ে কল করা কাজটি execute হবে।
// setTimeout() এর ভেতর চাইলে টাইম ও বলে দেয়া যাবে। এর মানে হচ্ছে, ঐ কাজটি মুলত কতক্ষন পর হবে!  



