// setTimeout এর মত আরেকটি বিষয় আছে সেটা হচ্ছে setInterval(), এটি ব্যবহার করে মুলত একটি কাজ কে নির্দিষ্ট সময় পর করানো যাবে। 
// ধরি আমাদের একটি ফাংশন আছে, এই ফাংশন কে আমরা ১ সেকেন্ড পর execute করতে চাচ্ছি। তাহলে আমরা এটি এভাবে করব -


setInterval(function() {
    console.log('Rahman'); 
}, 1000) // এক সেকেন্ড পর পর এই ফাংশনটি execute হবে। 