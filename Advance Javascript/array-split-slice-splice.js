// slice এর কাজ হচ্ছে, array এর নির্দিষ্ট অংশ কেটে নতুন একটা অ্যারে তৈরী করা। কিন্তু, এর জন্য মুল অ্যারে এর উপর কোন ইফেক্ট পড়বেনা। 
// slice করার সময়, কোন জায়গা থেকে সে কাটবে সেটার ইন্ডেক্স এবং কোন জায়গায় শেষ হবে সেটার ইন্ডেক্স নাম্বার দিতে হবে। 


const nums = [1,2,3,5,6,7,8,9];

// এখানে, বলা হচ্ছে slice হবে 2 নং array থেকে 5 array এর আগ পর্যন্ত কেটে, সেটা নিয়ে নতুন অ্যারে তৈরী করবে।
//const partOfArray = nums.slice(2, 5); // 3.5.6 

