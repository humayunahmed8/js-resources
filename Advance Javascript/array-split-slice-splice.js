// splice এর কাজ হচ্ছে, অ্যারে এর ভেতর থেকে ভ্যালু গুলো রিমুভ করে দিবে। এবং সে মুল অ্যারেকে পরিবর্তন করে ফেলবে। 
// নিচের উদাহরনে দেখানো হচ্ছে যে, কত নম্বর ইন্ডেক্স থেকে সে শুরু করবে, এবং তারপর কতগুলো কাটবে। 
// ধরি, nums.splice(2, 2) , এর মানে হচ্ছে আমাদের মুল array [1,2,3,5,6,7,8,9] এটার ২য় নম্বর ইন্ডেক্স থেকে শুরু করবে। ২য় নম্বর ইন্ডেক্স হচ্ছে 3, এবং তারপর ২ দিয়ে বলা হচ্ছে মোট কয়টি রিমুভ করবে। তাহলে আগে কেটেছে সে ২য় নম্বর ইন্ডেক্স 3, মোট কাটবে দুইটি তাহলে তারপর একটি, তারমানে ৫ কে রিমুভ করবে। তাহলে আমাদের মুল array টি হবে, [3,5] বাদে। এরকম - [ 1, 2, 6, 7, 8, 9 ]

// splice এ আরেকটি বিষয় করা যায়, রিমুভ হওয়া জায়গায় চাইলে নতুন ভ্যালু ইনজেক্ট করা যাবে। 

const arraySplice = nums.splice(2, 2, 72); // 3.5 বাদ দিয়ে তার জায়গায় নতুন ইন্ডেক্স 72 কে ইঞ্জেক্ট করবে। 
console.log(arraySplice); 

console.log(nums);



