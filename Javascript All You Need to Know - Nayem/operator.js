// Assignment Operator 
// +=, -=, *=, /=, %=

var x = 10;
var y = 20;

x = x + y;
// x += y;
console.log(x); // 10+20 = 30

x -= y;
console.log(x); // 30 - 20 = 10

x *= y;
console.log(x); // 10 * 20 = 200

x /= y;
console.log(x); // 200 / 20 = 10

x %= y; 
console.log(x); // 10 / 20 = 10


// Comparison Operator 
// ==, !==, >, <, >=, <=

var c = 10;
var d = 20;
console.log(c == d); // false
console.log(c != d); // true
console.log(c > d); // false
console.log(c < d); // true
console.log(c <= d); // true
console.log(c >= d); // false
console.log(c !== d); // number + number = true, 


