//  Arithmetic Operator 
// +, - , * , / , %

var a = 10;
var b = 20;

console.log(a + b); // 30
console.log(a - b); // -10
console.log(a * b); // 200
console.log(b / a); // (10/20) = 2
console.log(a / b); // (20/10) = 0.5 
console.log(a % b); // 10

