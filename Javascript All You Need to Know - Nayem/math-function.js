var number = 4.555;

// Built in Math Function (absolute, floor, ceil, round)
console.log(Math.abs(number)); //  4.555
console.log(Math.floor(number)); //  4 
console.log(Math.ceil(number)); //  5
console.log(Math.round(number)); //  5 (.49 = 4, .50 = 5)

// Minimum or Maximum 
console.log(Math.max(400, 500, 200)); // 500
console.log(Math.min(400, 500, 200)); // 200

// Power (Math.pow(2, 3))
console.log(Math.pow(2, 3)); // 8
console.log(Math.pow(3, 3)); // 27



// square root
console.log(Math.sqrt(9)); // 3
console.log(Math.sqrt(64)); // 8
console.log(Math.sqrt(49)); // 7


// random number (Math.random())
console.log(Math.random());

console.log(Math.round(Math.random() * 10 + 1)) // Get random number inside 10







